package polymorphism1;

public class LgTV {
	public void on() {
		System.out.println("LgTV - 전원 On");
	}
	public void off() {
		System.out.println("LgTV - 전원 Off");
	}
	public void soundUp() {
		System.out.println("LgTV - 소리 크게");
	}
	public void soundDown() {
		System.out.println("LgTV - 소리 작게");
	}
}
